class ifelse{
    public static void main(String[] args){
        int score = 90;
        if(score > 50)
        {
            System.out.println("A Grade");
        }
        else if (score <= 40)
        {
            System.out.println("C Grade");
        }
        else
        {
            System.out.println("B Grade");
        }
    }
}